int		ft_atoi(char *str)
{
	int i;
	int j;
	int sign;

	i = 0;
	j = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if ((str[i] == 45 || str[i] == 43) && (str[i + 1] > 47 && str[i + 1] < 58))
	{
		if (str[i] == 45)
			sign = -1;
		i++;
	}
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		j = j + (str[i] - 48);
		if (str[i + 1] >= '0' && str[i + 1] <= '9')
			j = j * 10;
		i++;
	}
	return (sign * j);
}
