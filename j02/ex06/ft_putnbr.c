int		ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int i;

	i = 1;
	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		nb = 147483648;
	}
	while ((nb / i) != 0)
	{
		i = i * 10;
	}
	i = i / 10;
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	while (i > 0)
	{
		ft_putchar(nb / i % 10 + 48);
		i = i / 10;
	}
}
